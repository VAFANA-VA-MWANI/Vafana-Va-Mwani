                <div class="card-body">
                  <p class="lead text-white">">A revista foi fundada em 2015 por dois jornalistas, Eddie Pipocas (Edwaldo Pegado) e Vanessa Sanches, que se conheceram enquanto trabalhavam no portal SAPO, um dos maiores em Portugal. Os fundadores iniciaram o projeto com o desejo de poder trabalhar em uma linha editorial que refletisse o pensamento deles enquanto jornalistas negros e, assim, a revista nasceu e cresceu em Angola, de onde os dois trabalhavam na época, diretamente para a Sapo Internacional.[1] Se no início a revista apresentava uma temática generalista, atualmente seu conteúdo é focado em cultura urbana, especialmente em música, dos países africanos de língua lusófona e sua diáspora. Além de seus dois co-fundadores, o projeto conta com mais três jornalistas em Portugal e outros três em Angola.[2]
                        Em setembro de 2020, o prédio onde ficava a redação pegou fogo, ardendo o escritório da BANTUMEN e a casa de treze famílias - incluindo a do fundador Eddie Pipocas. O incêndio destruiu computadores e materiais de vídeo e fotografia; além de alguns importantes trabalhos como as duas horas de entrevista com a equipe do premiado filme Ao Fim do Mundo, de Basil da Cunha. Após a tragédia, um dos seguidores da revista teve a iniciativa de criar um financiamento coletivo para ajudar o projeto a voltar aos trabalhos editoriais e em pouco tempo a meta de quatro mil euros foi ultrapassada. [3][4]
                        No mesmo ano, em parceria com a organização SOS Racismo, a plataforma co-produziu o documentário Olhares sobre o racismo, de Bruno Cabral, Eddie Pipocas e Dércio Ferreira, como parte de uma iniciativa do DocLisboa. [5][1][6]
                        Uma das iniciativas mais recentes da plataforma reuniu, em 2021, uma lista com as cem personalidades negras mais influentes da lusofonia: a Bantumen Power List. A listagem tem por objectivo reconhecer profissionais negros e lusófonos de diversas áreas de atuação que promovam um sentido de representatividade para esta comunidade. A activista Grada Kilomba, a jornalista Conceição Queiroz, o músico Dino D’Santiago e o futebolista Reinildo, são alguns dos nomes agraciados com a distinção anual.[7][8] A cerimônia de divulgação da lista aconteceu no Teatro Municipal São Luiz em Lisboa, em uma iniciativa promovida em conjunto com a Empresa de Gestão de Equipamentos e Animação Cultural (EGEAC). Dentre os nomeados, dezessete nomes são de personalidades da Angola, dezessete do Brasil, quinze de Cabo Verde, nove da Guiné-Bissau, dez de Moçambique, sete de São Tomé e Príncipe e vinte e cinco de Portugal</p>
